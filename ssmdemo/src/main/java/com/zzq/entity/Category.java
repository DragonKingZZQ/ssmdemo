package com.zzq.entity;

public class Category {
	private Integer cid;  //编号
	private String cname;  //一级分类名
	public Integer getCid() {
		return cid;
	}
	public void setCid(Integer cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	
}
